CREATE TABLE ACCOUNT
(
    'ID' VARCHAR(45) NOT NULL COMMENT '아이디',
    'ADMIN' INTEGER NOT NULL COMMENT '관리자',
    'NAME' VARCHAR(45) NOT NULL COMMENT '이름',
    'PASSWORD' VARCHAR(45) NOT NULL COMMENT '비밀번호',
    'GENDER' VARCHAR(45) NOT NULL COMMENT '성별',
    'PHONE' VARCHAR(45) NOT NULL COMMENT '핸드폰',
    'EMAIL' VARCHAR(45) NOT NULL COMMENT '이메일',
    'SCHOOL' VARCHAR(45) NOT NULL COMMENT '학교정보',
    'CHATTING' VARCHAR(45) NOT NULL COMMENT '채팅구분코드',
    PRIMARY KEY (ID)
);

CREATE TABLE CHATTING
(
    'ID' VARCHAR(45) NOT NULL COMMENT '아이디',
    'CHATTING' VARCHAR(45) NOT NULL COMMENT '채팅내용',
    'CHATTINGTIME' VARCHAR(45) NOT NULL COMMENT '채팅시간'
    PRIMARY KEY (ID)
);

CREATE TABLE REVIEW
(
    'ID' VARCHAR(45) NOT NULL COMMENT '아이디',
    'CHATSCORE' INT COMMENT '평점'
    'REVIEW' VARCHAR(45) '리뷰 내용'
    PRIMARY KEY (ID)
);